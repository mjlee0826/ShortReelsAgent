# --------------------------------------------------------------------
# 素材過濾 (ContextCompressor)
# --------------------------------------------------------------------
# MUSIQ 技術畫質分數低於此值的素材會直接淘汰，避免低畫質影片進入導演決策
TECHNICAL_SCORE_FILTER_THRESHOLD = 40.0


# --------------------------------------------------------------------
# 視覺評分模型 (MusiqModelManager)
# --------------------------------------------------------------------
# MUSIQ 推論前 PIL 圖片的最大短邊長度（過大會吃 VRAM 且分數震盪）
MUSIQ_MAX_INPUT_SIZE = 512


# --------------------------------------------------------------------
# 物理切點偵測 (SceneCutExtractor)
# --------------------------------------------------------------------
# PySceneDetect ContentDetector 的像素變化閾值，越低越敏感
SCENE_CUT_THRESHOLD = 27.0


# --------------------------------------------------------------------
# 導演糾錯流程 (ReflectionState)
# --------------------------------------------------------------------
# Critic 判定失敗後最多重新生成幾次，超過則強制接受當前草稿避免無限扣 API 費用
DIRECTOR_MAX_RETRIES = 3


# --------------------------------------------------------------------
# 雲端視覺推理 (GeminiModelManager)
# --------------------------------------------------------------------
# Gemini 影片上傳後台處理輪詢的最大次數（150 * 2s = 5 分鐘）
GEMINI_VIDEO_PROCESSING_MAX_POLL = 150
GEMINI_VIDEO_POLL_INTERVAL_SEC = 2


# --------------------------------------------------------------------
# 音樂快取 (MediaDownloader)
# --------------------------------------------------------------------
# music_cache 保留的最大檔案數量（超過會清最舊的）
MUSIC_CACHE_MAX_FILES = 20
# 「最近被觸碰過」的保護期，這段時間內被 utime 過的檔案不會被清掉
# 用於防止使用者尚未播放完的 BGM 在 cleanup 時被刪
MUSIC_CACHE_PROTECTION_WINDOW_SEC = 3600


# --------------------------------------------------------------------
# Whisper 幻覺防跳針 (WhisperModelManager)
# --------------------------------------------------------------------
# 連續「停滯型重複」達到此次數，判定為 attention 鎖死並截斷後續輸出
HALLUCINATION_REPEAT_THRESHOLD = 3
# 時間戳記停滯的容差（秒）：若下個 chunk 的 start_time 不超過上個 end_time + 此值，
# 即視為時間沒有正常推進（合法的歌詞副歌時間會持續推進，因此不會被誤殺）
HALLUCINATION_TIME_STALL_TOLERANCE_SEC = 0.1


# --------------------------------------------------------------------
# 全域 FPS 決策 (DirectorFacade)
# --------------------------------------------------------------------
# 來源素材若有任何影片達到此 FPS，輸出全域設定為 DEFAULT_FPS_HIGH
HIGH_FPS_THRESHOLD = 50.0
DEFAULT_FPS_HIGH = 60
DEFAULT_FPS_LOW = 30
